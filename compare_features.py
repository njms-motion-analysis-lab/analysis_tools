for sensor_code, models in data:
    # Initialize a nested dictionary for the sensor code
    rankings[sensor_code] = {}
    
    # Assuming 'RandomForest' is the only model (modify if there are more)
    model_data = models['RandomForest']
    
    # Collect all folds
    folds = set()
    for feature in model_data.values():
        folds.update(feature['sums'].keys())
    folds = sorted(folds)
    
    # For each fold, compute the rankings
    for fold in folds:
        # Collect sums for all features for this fold
        fold_sums = {}
        for feature_name, feature_data in model_data.items():
            sum_value = feature_data['sums'].get(fold, 0)
            fold_sums[feature_name] = sum_value
        
        # Sort features based on the sum values in descending order
        sorted_features = sorted(fold_sums.items(), key=lambda x: x[1], reverse=True)
        
        # Store the rankings
        rankings[sensor_code][fold] = sorted_features

